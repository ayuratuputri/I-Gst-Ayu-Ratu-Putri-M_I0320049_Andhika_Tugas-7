import math
a = 89.9
b = 70.5
print("a = ", a)
print("b = ", b)
#mencari nilai max dan min
print("nilai maximum = ", max(a,b))
print("nilai minimum = ", min(a,b))

#pembulatan bilangan riil ke bilangan bulat dibawahnya
print("math.floor a = ", math.floor(a))
print("math.floor b = ", math.floor(b))

#pembulatan bilangan rril ke bilangan bulat diatasnya
print("nilai a ceil = ", math.ceil(a))
print("nilai b ceil = ", math.ceil(b))